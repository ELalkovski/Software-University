namespace StringsAndChars
{
    using System;

    class StringsAndChars
    {
        static void Main(string[] args)
        {
            string firstStr = "Software University";
            char firstChar = 'B';
            char secondChar = 'y';
            char thirdChar = 'e';
            string secondStr = "I love programming";

            Console.WriteLine(firstStr);
            Console.WriteLine(firstChar);
            Console.WriteLine(secondChar);
            Console.WriteLine(thirdChar);
            Console.WriteLine(secondStr);
        }
    }
}
