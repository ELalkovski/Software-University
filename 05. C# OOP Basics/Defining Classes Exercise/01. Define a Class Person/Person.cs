namespace _01.Define_a_Class_Person
{
    class Person
    {
        public string name;
        public int age;

        public string Name
        {
            get { return this.name; }
            set { this.name = value; }
        }

        public int Age
        {
            get { return this.age; }
            set { this.age = value; }
        }
    }
}
